    String columns[] = {"Id", "Roll Number", "Name", "Father Name", "Age", "Email", "Department"};
    DefaultTableModel studentDefaultTableModel;

    private void fillStudentTable() {
        studentDefaultTableModel = new DefaultTableModel(columns, 0);
        try {
            List<Task3.model.Student> students = studentDbManager.getAllStudents();
            for (Task3.model.Student student : students) {
                Object row[] = {student.getId(), student.getRollNumber(), student.getName(), student.getFatherName(),
                    student.getAge(), student.getEmail(), student.getDepartment().getName()};
                studentDefaultTableModel.addRow(row);
                studentTable.setModel(studentDefaultTableModel);
            }

            studentTable.getColumnModel().getColumn(0).setWidth(0);
            studentTable.getColumnModel().getColumn(0).setMinWidth(0);
            studentTable.getColumnModel().getColumn(0).setMaxWidth(0);

        } catch (SQLException ex) {
            Logger.getLogger(StudentManagementSystemFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void fillDepartmentCombox() {
        List<Department> departments = departmentDbManager.getAllDepartments();
        for (Department department : departments) {
            departmentCombox.addItem(department.getName());
        }
    }

    private Task3.model.Student getStudent() {

        Task3.model.Student student = new Task3.model.Student();
        try {
            String rollNumber = rollNumberTextLabel.getText();
            String name = nameTextLabel.getText();
            String fatherName = fatherNameTextLabel.getText();
            Integer age = Integer.parseInt(ageTextLabel.getValue().toString());
            String email = emailTextLabel.getText();
            String departmentName = departmentCombox.getSelectedItem().toString();

            Department department = departmentDbManager.getDepartmentByName(departmentName);
            
            student.setRollNumber(rollNumber);
            student.setName(name);
            student.setFatherName(fatherName);
            student.setAge(age);
            student.setEmail(email);
            student.setDepartment(department);

        } catch (SQLException ex) {
            Logger.getLogger(StudentManagementSystemFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

        return student;

    }

