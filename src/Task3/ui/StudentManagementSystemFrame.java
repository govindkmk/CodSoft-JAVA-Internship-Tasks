    private void studentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentTableMouseClicked
        try {
            // TODO add your handling code here:

            studentId = Integer.parseInt(studentTable.getValueAt(studentTable.getSelectedRow(), 0).toString());
            Student student = studentDbManager.getStudentById(studentId);
            rollNumberTextLabel.setText(student.getRollNumber());
            nameTextLabel.setText(student.getName());
            fatherNameTextLabel.setText(student.getFatherName());
            ageTextLabel.setValue(student.getAge());
            emailTextLabel.setText(student.getEmail());
            Department department = departmentDbManager.getDepartmentByName(student.getDepartment().getName());
            departmentCombox.setSelectedItem(department.getName());

        } catch (SQLException ex) {
            Logger.getLogger(StudentManagementSystemFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_studentTableMouseClicked

